# ‚ïî‚ïê‚ï° 011c89a7-f3d1-4cea-b71a-3d6f96a1801c
begin
function solid_body_rotation(np,nz)
    Œì=simple_periodic_domain(np)
    Œì = UnitGrid(Œì.XC.grid;option="full")
    Œ≥=Œì.XC.grid;
    
    #Solid-body rotation around central location ...
    i=Int(np/2+1)
    u=-(Œì.YG.-Œì.YG[1][i,i])
    v=(Œì.XG.-Œì.XG[1][i,i])
    
    #... plus a convergent term to / from central location
    d=-0.01
    u=u+d*(Œì.XG.-Œì.XG[1][i,i])
    v=v+d*(Œì.YG.-Œì.YG[1][i,i])
    
    #Replicate u,v in vertical dimension
    uu=MeshArray(Œ≥,Œ≥.ioPrec,nz)
    [uu[k]=u[1] for k=1:nz]
    vv=MeshArray(Œ≥,Œ≥.ioPrec,nz)
    [vv[k]=v[1] for k=1:nz]
    
    #Vertical velocity component w    
    w=fill(-0.01,MeshArray(Œ≥,Œ≥.ioPrec,nz+1))
    
    return write(uu),write(vv),write(w)
end
	
